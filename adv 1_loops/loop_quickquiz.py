# Quick quiz 1
# Write a program to print 1 to 50 using a while loop

j = 0
while j !=51:
    print(j)
    j = j + 1
print("\n")

# Quick quiz 2
# Write a program to print the content of a list using while loops

fruits = ['Banana', 'Watermelon', 'Grape', 'Mango']
i = 0
while i<len(fruits):
    print(fruits[i])
    i = i + 1
print("\n")


