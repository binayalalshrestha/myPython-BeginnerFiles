# Create a list using []
a = [1, 2, 4, 56, 6]

# Print the list using print() function
print(a)

# Access using index usong a[0], a[1], a[2]
print(a[2])

# Change the value of list using
a[0]=98
print(a)

# We can create a list with items of any data types
c = [ 45, "Binay", False, 6.9]
print(c)

# List Slicing
friends = ["Binay", "Lishal", "Samman", "Digesh", "Reeya", 45]
print(friends[0:4])
print(friends[-4:-1])