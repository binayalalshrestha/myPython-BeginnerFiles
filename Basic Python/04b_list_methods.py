l1 = [1,8,7,2,21,15]
print(l1)

#sort
l1.sort()
print(l1)

#reverse
l1.reverse()
print(l1)

#append / adds value at the end of the list
l1.append(13)
l1.sort()
print(l1)

#insert
l1.insert(2,10)
print(l1)

#pop /needs index of list-removes the value in index
l1.pop(2)
print(l1)

#remove / removes the value in the list
l1.remove(21)
print(l1)